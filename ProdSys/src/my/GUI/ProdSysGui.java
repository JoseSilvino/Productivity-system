    private int search_project(String nome) {
      int t = this.projects.size();
      for(int i = 0 ; i <t ; i ++) {
          if(projects.get(i).getName().equals(nome)) {
              return i;
          }
      }
      return -1;
    }
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
         String nome =   JOptionPane.showInputDialog("Nome do Projeto?");
         int index = search_project(nome);
         if(index==-1) {
             JOptionPane.showMessageDialog(null,"Este projeto não existe");
         } else {
             if(projects.get(index).getClass().equals(Desenvolvimento.class)) {
                String dec = JOptionPane.showInputDialog("Remover colaborador/Adicionar Colaborador");
             String colab = JOptionPane.showInputDialog("Nome do colaborador :");
             dec = dec.toLowerCase();
             if(dec.equals("remover")) {
                 if(((Desenvolvimento)projects.get(index)).contem(colab)) {
                     ((Desenvolvimento)projects.get(index)).removeColaborador(((Desenvolvimento)projects.get(index)).indice(colab));
                     collabs.get(colab).remvProjeto((Desenvolvimento)projects.get(index));
                 }
             } else if(dec.equals("adicionar")) {
                 if(collabs.containsKey(colab)) {
                ((Desenvolvimento)projects.get(index)).addColaborador(this.collabs.get(colab));
                this.collabs.get(colab).addProjeto((Desenvolvimento)projects.get(index));
                 } else {
                     JOptionPane.showMessageDialog(null, colab + "Não existe");
                 }
             }
             } else if(projects.get(index).getClass().equals(Andamento.class)) {
              String numero =   JOptionPane.showInputDialog("Quantos autores a publicação terá?");
              try {
                  int n =Integer.parseInt(numero);
                  ArrayList<String> aut = new ArrayList<>();
                  for(int i = 0 ; i < n ; i ++) {
                      aut.add(JOptionPane.showInputDialog("diga o nome do autor "+ (n-i) + " faltando"));
                  }
                  String obj = JOptionPane.showInputDialog("Diga o conteudo da publicação");
                  Publicação pub = new Publicação(obj,aut);
                  ((Andamento)projects.get(index)).addPub(pub);
                  this.publications.add(pub);
                  for(int i = 0 ; i < aut.size() ; i++) {
                      collabs.get(aut.get(i)).addPub(pub);
                  }
              }catch(NumberFormatException e) {
                  JOptionPane.showMessageDialog(null,"Número inválido");
              }
             }
         }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        String nome = JOptionPane.showInputDialog("Nome do Projeto");
        int index = this.search_project(nome);
        if(projects.get(index).getClass().equals(Desenvolvimento.class)) {
        Desenvolvimento desen = (Desenvolvimento)this.projects.get(index);
        if(desen.paraAndamento()) {
        projects.remove(index);
        //pegar data do inicio
        String data = JOptionPane.showInputDialog("Data de início (dia/mes/ano)");
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            try {
                Calendar day = Calendar.getInstance();
                day.setTime(sdf.parse(data));
                Andamento proj =new Andamento(desen.getName(),desen.getMoney(),desen.getAgency(),desen.getObjective(),desen.getDescription(),desen.getCollabs(),day);
                projects.add(proj);
                for(int i = 0 ; i < desen.getCollabs().size();i++) {
                    desen.getCollab(i).remvProjeto(desen);
                    desen.getCollab(i).addProjeto(proj);
                }
            } catch (ParseException ex) {
                
                JOptionPane.showMessageDialog(null,"Formato inválido , tente o formato dia/mes/ano , exemplo : 01/05/2010");
                Logger.getLogger(ProdSysGui.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Não pode alterar estado pois um Aluno de Graduação ja esta em um projeto em Andamento");
        }
        } else if(projects.get(index).getClass().equals(Andamento.class)) {
            if(((Andamento)projects.get(index)).getPubSize()!=0) {
                Andamento and = (Andamento)projects.get(index);
                projects.remove(index);
                //pegar data do fim
                String data = JOptionPane.showInputDialog("Data de término (dia/mes/ano)");
                SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
                try {
                    Calendar day = Calendar.getInstance();
                    day.setTime(sdf.parse(data));
                    Concluido proj = new Concluido(and.getName(),and.getMoney(),and.getAgency(),and.getObjective(),and.getDescription(),and.getCollabs(),and.getPubs(),day,and.getStart());
                     projects.add(proj);
                     for(int i = 0 ; i < and.getCollabs().size();i++) {
                         and.getCollab(i).remvProjeto(and);
                         and.getCollab(i).addProjeto(proj);
                     }
                }catch(ParseException ex) {
                    JOptionPane.showMessageDialog(null,"Formato inválido , tente o formato dia/mes/ano , exemplo : 01/05/2010");
                    Logger.getLogger(ProdSysGui.class.getName()).log(Level.SEVERE, null, ex);
                }
               
            } else {
                JOptionPane.showMessageDialog(null,"Não pode alterar estado pois faltam publicações ligadas ao projeto antes de finalizar");
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton7ActionPerformed
    private int Search_By_State(int st) {
        int t = projects.size(),n=0;
        for(int i = 0 ; i < t ; i ++) {
            if(st==1&&projects.get(i).getClass().equals(Desenvolvimento.class)) {
                n++;
            }
            else if(st==2&&projects.get(i).getClass().equals(Andamento.class)) {
                n++;
            }
            else if(st==3&& projects.get(i).getClass().equals(Concluido.class)) {
                n++;
            }
        } 
        return n;
    }
