    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String option = (String)jComboBox1.getSelectedItem();
        if(!this.collabs.containsKey(jTextField1.getText())) {
         Colaborador novo ;
        switch (option) {
            case "Professor":
                novo = new Professor(jTextField1.getText(),jTextField2.getText());
                break;
            case "Aluno de Graduação":
                novo = new AlunoDeGraduação(jTextField1.getText(),jTextField2.getText());
                break;
            default:
                novo =new Colaborador(jTextField1.getText(),jTextField2.getText());
                break;
        }
        collabs.put(novo.getName(),novo);
        JOptionPane.showMessageDialog(null,novo.getName() + " Foi criado");
        }
        else  {
            JOptionPane.showMessageDialog(null,"Este colaborador já existe");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

